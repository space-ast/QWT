/**
 * @brief Constructor
 */
QwtPickerClickRectMachine::QwtPickerClickRectMachine() : QwtPickerMachine(RectSelection)
{
}

/**
 * @brief Transition function that processes events and returns commands
 * @param[in] eventPattern Event pattern for matching
 * @param[in] event Event to process
 * @return List of commands generated by the transition
 */
QList< QwtPickerMachine::Command > QwtPickerClickRectMachine::transition(const QwtEventPattern& eventPattern,
                                                                         const QEvent* event)
{
    QList< QwtPickerMachine::Command > cmdList;

    switch (event->type()) {
    case QEvent::MouseButtonPress: {
        if (eventPattern.mouseMatch(QwtEventPattern::MouseSelect1, static_cast< const QMouseEvent* >(event))) {
            switch (state()) {
            case 0: {
                cmdList += Begin;
                cmdList += Append;
                setState(1);
                break;
            }
            case 1: {
                // Uh, strange we missed the MouseButtonRelease
                break;
            }
            default: {
                cmdList += End;
                setState(0);
            }
            }
        }
        break;
    }
    case QEvent::MouseMove:
    case QEvent::Wheel: {
        if (state() != 0)
            cmdList += Move;
        break;
    }
    case QEvent::MouseButtonRelease: {
        if (eventPattern.mouseMatch(QwtEventPattern::MouseSelect1, static_cast< const QMouseEvent* >(event))) {
            if (state() == 1) {
                cmdList += Append;
                setState(2);
            }
        }
        break;
    }
    case QEvent::KeyPress: {
        const QKeyEvent* keyEvent = static_cast< const QKeyEvent* >(event);
        if (eventPattern.keyMatch(QwtEventPattern::KeySelect1, keyEvent)) {
            if (!keyEvent->isAutoRepeat()) {
                if (state() == 0) {
                    cmdList += Begin;
                    cmdList += Append;
                    setState(1);
                } else {
                    if (state() == 1) {
                        cmdList += Append;
                        setState(2);
                    } else if (state() == 2) {
                        cmdList += End;
                        setState(0);
                    }
                }
            }
        }
        break;
    }
    default:
        break;
    }

    return cmdList;
}

