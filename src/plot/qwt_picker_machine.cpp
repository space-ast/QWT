/**
 * \if ENGLISH
 * @brief Transition function that processes events and returns commands
 * @param[in] eventPattern Event pattern for matching
 * @param[in] e Event to process
 * @return List of commands generated by the transition
 * \endif
 * \if CHINESE
 * @brief 处理事件并返回命令的转换函数
 * @param[in] eventPattern 用于匹配的事件模式
 * @param[in] e 要处理的事件
 * @return 转换生成的命令列表
 * \endif
 */
QList< QwtPickerMachine::Command > QwtPickerTrackerMachine::transition(
    const QwtEventPattern&, const QEvent* e )
{
    QList< QwtPickerMachine::Command > cmdList;

    switch ( e->type() )
    {
        case QEvent::Enter:
        case QEvent::MouseMove:
        {
            if ( state() == 0 )
            {
                cmdList += Begin;
                cmdList += Append;
                setState( 1 );
            }
            else
            {
                cmdList += Move;
            }
            break;
        }
        case QEvent::Leave:
        {
            cmdList += Remove;
            cmdList += End;
            setState( 0 );
        }
        default:
            break;
    }

    return cmdList;
}

