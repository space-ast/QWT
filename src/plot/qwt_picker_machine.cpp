#include "qwt_picker_machine.h"
#include "qwt_event_pattern.h"

#include <qevent.h>

/**
 * @brief Constructor
 * @param[in] type Selection type for this state machine
 */
QwtPickerMachine::QwtPickerMachine(SelectionType type) : m_selectionType(type), m_state(0)
{
}

/**
 * @brief Destructor
 */
QwtPickerMachine::~QwtPickerMachine()
{
}

/**
 * @brief Return the selection type
 * @return Selection type of this state machine
 */
QwtPickerMachine::SelectionType QwtPickerMachine::selectionType() const
{
    return m_selectionType;
}

/**
 * @brief Return the current state
 * @return Current state value
 */
int QwtPickerMachine::state() const
{
    return m_state;
}

/**
 * @brief Change the current state
 * @param[in] state New state value
 */
void QwtPickerMachine::setState(int state)
{
    m_state = state;
}

/**
 * @brief Set the current state to 0 (initial state)
 */
void QwtPickerMachine::reset()
{
    setState(0);
}

/**
 * @brief Constructor
 */
QwtPickerTrackerMachine::QwtPickerTrackerMachine() : QwtPickerMachine(NoSelection)
{
}

/**
 * @brief Transition function that processes events and returns commands
 * @param[in] eventPattern Event pattern for matching
 * @param[in] e Event to process
 * @return List of commands generated by the transition
 */
QList< QwtPickerMachine::Command > QwtPickerTrackerMachine::transition(const QwtEventPattern&, const QEvent* e)
{
    QList< QwtPickerMachine::Command > cmdList;

    switch (e->type()) {
    case QEvent::Enter:
    case QEvent::MouseMove: {
        if (state() == 0) {
            cmdList += Begin;
            cmdList += Append;
            setState(1);
        } else {
            cmdList += Move;
        }
        break;
    }
    case QEvent::Leave: {
        cmdList += Remove;
        cmdList += End;
        setState(0);
    }
    default:
        break;
    }

    return cmdList;
}

/**
 * @brief Constructor
 */
QwtPickerClickPointMachine::QwtPickerClickPointMachine() : QwtPickerMachine(PointSelection)
{
}

/**
 * @brief Transition function that processes events and returns commands
 * @param[in] eventPattern Event pattern for matching
 * @param[in] event Event to process
 * @return List of commands generated by the transition
 */
QList< QwtPickerMachine::Command > QwtPickerClickPointMachine::transition(const QwtEventPattern& eventPattern,
                                                                          const QEvent* event)
{
    QList< QwtPickerMachine::Command > cmdList;

    switch (event->type()) {
    case QEvent::MouseButtonPress: {
        if (eventPattern.mouseMatch(QwtEventPattern::MouseSelect1, static_cast< const QMouseEvent* >(event))) {
            cmdList += Begin;
            cmdList += Append;
            cmdList += End;
        }
        break;
    }
    case QEvent::KeyPress: {
        const QKeyEvent* keyEvent = static_cast< const QKeyEvent* >(event);
        if (eventPattern.keyMatch(QwtEventPattern::KeySelect1, keyEvent)) {
            if (!keyEvent->isAutoRepeat()) {
                cmdList += Begin;
                cmdList += Append;
                cmdList += End;
            }
        }
        break;
    }
    default:
        break;
    }

    return cmdList;
}

