/**
 * \if ENGLISH
 * @brief Transition function that processes events and returns commands
 * @param[in] eventPattern Event pattern for matching
 * @param[in] event Event to process
 * @return List of commands generated by the transition
 * \endif
 * \if CHINESE
 * @brief 处理事件并返回命令的转换函数
 * @param[in] eventPattern 用于匹配的事件模式
 * @param[in] event 要处理的事件
 * @return 转换生成的命令列表
 * \endif
 */
QList< QwtPickerMachine::Command > QwtPickerClickPointMachine::transition(
    const QwtEventPattern& eventPattern, const QEvent* event )
{
    QList< QwtPickerMachine::Command > cmdList;

    switch ( event->type() )
    {
        case QEvent::MouseButtonPress:
        {
            if ( eventPattern.mouseMatch( QwtEventPattern::MouseSelect1,
                static_cast< const QMouseEvent* >( event ) ) )
            {
                cmdList += Begin;
                cmdList += Append;
                cmdList += End;
            }
            break;
        }
        case QEvent::KeyPress:
        {
            const QKeyEvent* keyEvent = static_cast< const QKeyEvent* > ( event );
            if ( eventPattern.keyMatch( QwtEventPattern::KeySelect1, keyEvent ) )
            {
                if ( !keyEvent->isAutoRepeat() )
                {
                    cmdList += Begin;
                    cmdList += Append;
                    cmdList += End;
                }
            }
            break;
        }
        default:
            break;
    }

    return cmdList;
}

